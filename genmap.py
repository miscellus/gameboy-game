collision_map = []
for y in range(len(the_map)):
	cl = []
	for x in range(len(the_map[y])):
		t = lambda dx,dy: 1 if the_map[(y + dy) % len(the_map)][(x + dx) % len(the_map[y])] not in (0,1,2) else 0
		c = (
				(t(0,0) << 0) |
				(t(1,0) << 1) |
				(t(0,1) << 2) |
				(t(1,1) << 3) |
				((t(0,0) | t(1,0) | t(0,1) | t(1,1)) << 4) |
				((t(2,0) | t(2,1)) << 5) |
				((t(0,2) | t(1,2)) << 6) |
				(t(2,2) << 7)
			)

		cl.append(c)
	collision_map.append(cl)

