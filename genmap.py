collision_map = []
for y in range(len(the_map)):
	cl = []
	for x in range(len(the_map[y])):
		t = lambda dx,dy: the_map[(y + dy) % len(the_map)][(x + dx) % len(the_map[y])]
		c = 0
		if t(0,0) != 0:
			c |= 1<<7
		if t(1,0) != 0:
			c |= 1<<6
		if t(0,1) != 0:
			c |= 1<<5
		if t(1,1) != 0:
			c |= 1<<4
		if t(0,2) != 0:
			c |= 1<<3
		if t(1,2) != 0:
			c |= 1<<2
		if t(2,0) != 0:
			c |= 1<<1
		if t(2,1) != 0:
			c |= 1<<0

		cl.append(c)
	collision_map.append(cl)

